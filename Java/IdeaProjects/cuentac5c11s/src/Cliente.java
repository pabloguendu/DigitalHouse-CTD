public class Cliente {
    private int nroCliente;
    private String apellido;
    private String dni;
    private String cuit;

    public Cliente(int nroCliente, String apellido, String dni, String cuit) {
        this.nroCliente = nroCliente;
        this.apellido = apellido;
        this.dni = dni;
        this.cuit = cuit;
    }
}
